// Prerendered pages are generated in Russian. Keep the first client value the
// same so hydration can finish before the saved preference is applied.
export const language = writable<Language>("ru");

export function initializeLanguage() {
  if (typeof window === "undefined") return;
  const stored = window.localStorage.getItem("protocol150Language");
  const nextLanguage: Language = stored === "en" ? "en" : "ru";
  language.set(nextLanguage);
  document.documentElement.lang = nextLanguage;
}

export function setLanguage(nextLanguage: Language) {
  language.set(nextLanguage);
  if (typeof window !== "undefined") {
    window.localStorage.setItem("protocol150Language", nextLanguage);
    document.documentElement.lang = nextLanguage;
  }
}

export function toggleLanguage(currentLanguage: Language) {
  setLanguage(currentLanguage === "ru" ? "en" : "ru");
}

